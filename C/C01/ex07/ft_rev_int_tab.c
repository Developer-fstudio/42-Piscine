void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	c;
	int	tmp[size];

	i = size - 1;
	c = 0;
	while (i >= 0)
	{
		tmp[c] = tab[i];
		c++;
		i--;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tmp[i];
		i++;
	}
}
