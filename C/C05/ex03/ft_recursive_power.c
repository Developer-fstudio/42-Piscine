#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	int	result;

	result = nb;
	if (power > 1)
		return (nb * ft_recursive_power(nb, --power));
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	return (result);
}
